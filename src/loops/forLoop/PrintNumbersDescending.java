package loops.forLoop;

public class PrintNumbersDescending {
    public static void main(String[] args) {
        /*
        Print from 100-0. descending.

        start point : 100
        end point   : 0
        update      : decrement
         */

        for(int i = 100; i >=0; i--){
            System.out.println(i);
        }

    }
}
